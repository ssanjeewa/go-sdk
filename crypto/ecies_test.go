func TestDecryptSecret_TamperedCiphertext(t *testing.T) {
	kp, _ := GenerateKeyPair()
	enc, err := EncryptSecret(kp.PublicKey, big.NewInt(1337), big.NewInt(7331))
	if err != nil {
		t.Fatal(err)
	}

	// Flip one byte in the ciphertext region (offset 67 = after ephPubKey(65) + first 2 IV bytes).
	wireBytes, _ := hexDecode(enc[2:])
	wireBytes[90] ^= 0xFF
	tampered := "0x" + hexEncode(wireBytes)

	_, err = DecryptSecret(tampered, kp.PrivateKey)
	if err == nil {
		t.Fatal("expected error for tampered ciphertext")
	}
	var ce *CryptoError
	if !errors.As(err, &ce) {
		t.Errorf("expected *CryptoError, got %T: %v", err, err)
	}
}

