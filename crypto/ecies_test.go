// TestDecryptSecret_CrossCompat decrypts vectors from crypto/testdata/vectors.json.
// The vectors must be generated by zkp-middleware/internal/crypto/ecies.go to
// confirm byte-for-byte wire-format compatibility.
// This test is skipped if vectors.json contains no vectors.
func TestDecryptSecret_CrossCompat(t *testing.T) {
	type vector struct {
		PrivateKey      string `json:"privateKey"`
		PublicKey       string `json:"publicKey"`
		N               string `json:"n"`
		S               string `json:"s"`
		EncryptedSecret string `json:"encryptedSecret"`
	}
	type file struct {
		Vectors []vector `json:"vectors"`
	}

	data, err := os.ReadFile("testdata/vectors.json")
	if err != nil {
		t.Fatalf("cannot read testdata/vectors.json: %v", err)
	}
	var f file
	if err := json.Unmarshal(data, &f); err != nil {
		t.Fatalf("parse vectors.json: %v", err)
	}
	if len(f.Vectors) == 0 {
		t.Skip("no cross-compat vectors yet — populate testdata/vectors.json with middleware output")
	}

	for i, v := range f.Vectors {
		t.Run(string(rune('0'+i)), func(t *testing.T) {
			dec, err := DecryptSecret(v.EncryptedSecret, v.PrivateKey)
			if err != nil {
				t.Fatalf("DecryptSecret: %v", err)
			}
			wantN, _ := new(big.Int).SetString(v.N, 10)
			wantS, _ := new(big.Int).SetString(v.S, 10)
			if dec.N.Cmp(wantN) != 0 {
				t.Errorf("N mismatch: got %s, want %s", dec.N, wantN)
			}
			if dec.S.Cmp(wantS) != 0 {
				t.Errorf("S mismatch: got %s, want %s", dec.S, wantS)
			}
		})
	}
}

// hexEncode / hexDecode helpers to avoid importing encoding/hex in test body.
func hexEncode(b []byte) string {
	const hx = "0123456789abcdef"
	out := make([]byte, len(b)*2)
	for i, v := range b {
		out[i*2] = hx[v>>4]
		out[i*2+1] = hx[v&0xf]
	}
	return string(out)
}

func hexDecode(s string) ([]byte, error) {
	if len(s)%2 != 0 {
		return nil, nil
	}
	b := make([]byte, len(s)/2)
	for i := 0; i < len(s); i += 2 {
		hi := hexVal(s[i])
		lo := hexVal(s[i+1])
		b[i/2] = (hi << 4) | lo
	}
	return b, nil
}

func hexVal(c byte) byte {
	switch {
	case c >= '0' && c <= '9':
		return c - '0'
	case c >= 'a' && c <= 'f':
		return c - 'a' + 10
	case c >= 'A' && c <= 'F':
		return c - 'A' + 10
	}
	return 0
}
